<template>
  <div>
    <h1>User Directory</h1>
    <div class="search-bar-container">
      <div class="search-bar">
        <input
          id="search-box"
          type="text"
          v-model="searchQuery"
          placeholder="Search..."
        />
        <button @click="clearSearch" id="clear-button">Clear</button>
      </div>
    </div>
    <section v-if="errored">
      <p>
        We are not able to retrieve this data at the moment, please try again
        later
      </p>
    </section>
    <section v-else>
      <div v-if="loading">Loading...</div>
      <div
        class="search-results"
        v-else
        v-for="user in filteredUsers"
        :key="user.id"
      >
        <h2 class="user-name">{{ user.name }}</h2>
      </div>
    </section>
  </div>
</template>

<script>
import axios from "axios";

export default {
  data() {
    return {
      users: [],
      searchQuery: "",
      loading: true,
      errored: false,
    };
  },

  methods: {
    /* Asynchronous function to fetch list of users from API */
    async getUsers() {
      await axios
        .get("https://jsonplaceholder.typicode.com/users")
        .then((response) => {
          this.users = response.data;
        })
        .catch((error) => {
          console.log(error);
          this.errored = true;
        })
        .finally(() => (this.loading = false));
    },

    /* Function to clear the search query */
    clearSearch: function () {
      this.searchQuery = "";
    },
  },

  created() {
    this.getUsers();
  },

  computed: {
    /* Function to return new list of users based off the search query */
    filteredUsers: function () {
      return this.users.filter((user) => {
        return user.name.toLowerCase().match(this.searchQuery.toLowerCase());
      });
    },
  },
};
</script>

